class Clients:
    def __init__(self, name, email, phone):
        self.name = name
        self.email = email
        self.phone = phone


@property
def fullName(self):
    return '{} {}'.format(self.firstname, self.lastname)


def __repr__(self):
    return 'Cliente("{}", "{}", {} )'.format(self.firstname, self.lastname, self.email)
